/**
 * @param {number[]} nums
 */
var NumArray = function (nums) {
  this.sums = [];
  let sum = 0;
  for (let i = 0; i < nums.length; i++) {
    sum += nums[i];
    this.sums.push(sum);
  }
};

/**
 * @param {number} i
 * @param {number} j
 * @return {number}
 */
NumArray.prototype.sumRange = function (i, j) {
  return this.sums[j] - (i > 0 ? this.sums[i - 1] : 0);
};

