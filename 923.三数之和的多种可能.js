/**
 * @param {number[]} A
 * @param {number} target
 * @return {number}
 */
var threeSumMulti = function (A, target) {
  function fac(n) {
    let res = 1;
    for (let i = 1; i <= n; i++) {
      res *= i;
    }
    return res;
  }
  function C(m, n) {
    let mul = 1;
    for (let i = n + 1; i <= m; i++) {
      mul *= i;
    }
    return mul / fac(m - n);
  }
  if (A.length < 3) {
    return 0;
  }
  let count = 0;
  let map = new Map();
  for (let i = 0; i < A.length; i++) {
    if (map.has(A[i])) {
      map.set(A[i], map.get(A[i]) + 1);
    } else {
      map.set(A[i], 1);
    }
  }
  A = A.sort((a, b) => a - b);
  for (let i = 0; i <= A.length - 3; i++) {
    if (i > 0 && A[i] === A[i - 1]) continue;
    let [j, k] = [i + 1, A.length - 1];
    while (j < k) {
      let sum = A[i] + A[j] + A[k];
      if (sum < target) {
        j++;
      } else if (sum > target) {
        k--;
      } else {
        let count_i = map.get(A[i]);
        let count_j = map.get(A[j]);
        let count_k = map.get(A[k]);
        if (A[i] === A[j] && A[j] === A[k]) {
          count += C(count_i, count_i - 3);
          j += count_j;
        } else if (A[i] === A[j] && A[j] < A[k]) {
          count += C(count_j, 2) * count_k;
          j += count_j - 1;
        } else if (A[i] < A[j] && A[j] === A[k]) {
          count += count_i * C(count_k, 2);
          k -= count_k;
        } else {
          count += count_i * count_j * count_k;
          j += count_j;
          k -= count_k;
        }
      }
    }
  }
  return count % (10 ** 9 + 7);
};
